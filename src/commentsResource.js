import comments from "./utils/comments";

const commentsResource = wrapPromise(
  new Promise(resolve => setTimeout(() => resolve(comments), 5000))
);

function wrapPromise(promise) {
  let status = "pending";
  let result;
  let suspender = promise.then(
    response => {
      status = "success";
      result = response;
    },
    e => {
      status = "error";
      result = e;
    }
  );
  return {
    read() {
      if (status === "pending") {
        throw suspender;
      } else if (status === "error") {
        throw result;
      } else if (status === "success") {
        return result;
      }
    }
  };
}

export default commentsResource;
